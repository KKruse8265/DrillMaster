package restaurant.training.basis;

/*
 * Dieses Programm dient dem Erlernen von Java. Eine Praxisrelevanz ist nicht
 * beabsichtigt. Im imaginären Restaurant "Schmeckt Gut" sollen die üblichen
 * Abläufe informationstechnisch dargestellt werden.
 * 
 * Jeder kennt aus eigener Erfahrung einen Restaurantbesuch und was da so
 * passiert. Natürlich ist es in der Realität schwachsinnig einen Gast erst
 * instanzieren zu müssen, bevor man ihn zur Kenntnis nimmt, aber wie gesagt
 * Realität oder Computerunterstützung eines reellen Restaurants ist auch nicht
 * das Ziel.
 * 
 * 
 * im Restaurant gibt es eine Anzahl von Tischen jeder Tisch hat eine bestimmte
 * Anzahl von Plätzen jeder Tisch hat eine Nummer mit der er eindeutig
 * gekennzeichnet ist ein Tisch kann für eine bestimmte Zeit reserviert sein
 * 
 * Im restaurant arbeiten ein Wirt, der die Getränke zapft
 * 
 * Im Restaurant arbeiten Kellner, die die Bestellung der Gäste aufnehmen die
 * Bestellung an die Küche oder den Wirt weitergeben und die gewünschten
 * Gerichte/Getränke an den richtigen Tisch/Gast bringen außerdem kassieren sie
 * ab.
 * 
 * es gibt eine Speisekarte in der alle bestellbaren Gerichte aufgeführt sind
 * 
 * die Gäste wählen die Gerichte aus, Gäste können Tische reservieren
 * 
 * die Küche bereiten an Hand der Bestellung die Gerichte zu
 * 
 * jedes Gericht hat Zutaten die im Rezept stehen
 * 
 * am Ende des Tages, wird aus den verbrauchten Zutaten eine Einkaufsliste für
 * den Wirt generiert
 */